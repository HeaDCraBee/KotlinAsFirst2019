fun sequenceDigit(n: Int, x: String): Int {
    var i = 0
    var num = 0
    var a = 0
    while (i < n) {
        num++
        var value = 0
        if (x == "fib") value = fib(num)
        else if (x == "sqr") value = sqr(num)
        var k = 0
        val z = value
        while (value > 0) {
            k++
            value /= 10
        }
        i += k
        if (i < n) continue
        else {
            a = (z / 10.0.pow(i - n)).toInt() % 10
            break
        }
    }
    return a
}



